import { Translations } from '../locales';

export interface StatsCardOptions {
	title: string;
	value: string | number;
	icon?: string;
	subtitle?: string;
	trend?: 'up' | 'down' | 'neutral';
	trendValue?: string;
	color?: string;
	onClick?: () => void;
}

export function createStatsCard(container: HTMLElement, options: StatsCardOptions): HTMLElement {
	const card = container.createDiv({ cls: 'vd-stats-card' });
	
	if (options.color) {
		card.style.setProperty('--card-accent-color', options.color);
	}
	
	if (options.onClick) {
		card.addClass('vd-stats-card-clickable');
		card.addEventListener('click', options.onClick);
	}
	
	// Icon
	if (options.icon) {
		const iconEl = card.createDiv({ cls: 'vd-stats-card-icon' });
		iconEl.innerHTML = getIcon(options.icon);
	}
	
	// Content
	const content = card.createDiv({ cls: 'vd-stats-card-content' });
	
	// Title
	content.createDiv({ 
		cls: 'vd-stats-card-title', 
		text: options.title 
	});
	
	// Value
	const valueContainer = content.createDiv({ cls: 'vd-stats-card-value-container' });
	valueContainer.createSpan({ 
		cls: 'vd-stats-card-value', 
		text: String(options.value) 
	});
	
	// Trend
	if (options.trend && options.trendValue) {
		const trendEl = valueContainer.createSpan({ 
			cls: `vd-stats-card-trend vd-trend-${options.trend}` 
		});
		const trendIcon = options.trend === 'up' ? '↑' : options.trend === 'down' ? '↓' : '→';
		trendEl.setText(`${trendIcon} ${options.trendValue}`);
	}
	
	// Subtitle
	if (options.subtitle) {
		content.createDiv({ 
			cls: 'vd-stats-card-subtitle', 
			text: options.subtitle 
		});
	}
	
	return card;
}

export function createStatsCardGrid(container: HTMLElement): HTMLElement {
	return container.createDiv({ cls: 'vd-stats-grid' });
}

